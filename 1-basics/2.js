// Assignment Operators

let age = 18 + 8;

age += 2; // age = age + 2
age -= 2; // age = age - 2
age *= 2; // age = age * 2
age /= 2; // age = age / 2

age++; // age = age + 1;
age--; // age = age - 1;

console.log(age);

// Comparison Operators

const majorityAge = 18;

console.log(age > majorityAge);
console.log(age >= majorityAge);
console.log(age < majorityAge);
console.log(age <= majorityAge);
console.log(age == majorityAge);
