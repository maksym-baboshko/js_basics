// Operator Precedence

const box = 100;
const size = 90;
const isSuitable = box - 10 > size - 5;

console.log(isSuitable);
console.log((18 + 8) / 2);

let b;
let c;

b = c = 100 + 50;

console.log(b);
console.log(c);

